#include <stdio.h>
#include <string.h>

#include "jerryscript.h"
#include "jerryscript-ext/handler.h"
#include "js_libs.h"

#include "blob/main.js.h"

struct my_struct {
    float num;
    int arr[3];
} my_struct_t;

void pass_string_to_host(const char* str) {
    printf("Got string from app: %s\n", str);
}

void pass_string_to_host_via_function_handle(const char* (*func)(void)) {
    printf("Got string from app: %s\n", func());
}

void pass_string_to_app_via_function_handle(void (*func)(const char*)) {
    func("Greetings from host!\n");
}

void pass_bytes_to_host(const uint8_t* data, size_t len) {
    printf("Got data with length %d from app: %s\n", len, data);
}

void pass_bytes_to_host_via_function_handle(const uint8_t* (*func)(void)) {
    printf("Got data from app: %s\n", (const char*) func());
}

void pass_bytes_to_app_via_function_handle(void (*func)(const uint8_t*, size_t)) {
    func((const uint8_t*) "Bruce Stringsteen!\n", strlen("Bruce Stringsteen!\n"));
}

int* pass_pointer_to_app_via_pointer_handle(void) {
    static int i = 1337;
    return &i;
}

void pass_pointer_to_host_via_pointer_handle(int* ptr) {
    printf("Got pointer from app that points to: %d\n", *ptr);
}

void pass_array_to_host(int arr[3]) {
    printf("Got array with following elements from app: ");
    for (int i = 0; i < 3; ++i) {
        printf("%d ", arr[i]);
    }
    printf("\n");
}

void pass_array_to_app_via_function_handle(void (*func)(int arr[3])) {
    int a[3] = {11, 22, 33};
    func(a);
}

void pass_struct_to_host(phydat_t s) {
    printf("Got struct with following fields from app: ");
    printf("val %d %d %d, unit %d, scale %d\n", s.val[0], s.val[1], s.val[2], s.unit, s.scale);
}

phydat_t pass_struct_to_app_via_return(void) {
    phydat_t data = {
        .val = {111, 222, 333},
        .unit = 42,
        .scale = 17
    };
    return data;
}

void pass_struct_to_app_via_function_handle(void (*func)(phydat_t s)) {
    phydat_t data = {
        .val = {111, 222, 333},
        .unit = 42,
        .scale = 17
    };
    func(data);
}

void pass_struct_to_host_via_function_handle(phydat_t (*func)(void)) {
    phydat_t s = func();
    printf("Got struct with following fields from app: ");
    printf("val %d %d %d, unit %d, scale %d\n", s.val[0], s.val[1], s.val[2], s.unit, s.scale);
}

int main(void) {
    jerry_value_t main_app, ret_value;
    int res = 0;

    // initialize engine
    jerry_init(JERRY_INIT_EMPTY);

    // register print function
    jerryx_handler_register_global((const jerry_char_t *) "print",
                                   jerryx_handler_print);

    // register natives grouped by libraries generated by RIOT LibGen to global object
    if (!js_libs_register_playground_natives()) {
        printf("Could not register natives!\n");
        res = 1;
    }

    // parse and run main.js
    main_app = jerry_parse(NULL, 0, main_js, main_js_len, JERRY_PARSE_NO_OPTS);
    if (!jerry_value_is_error(main_app)) {
        ret_value = jerry_run(main_app);

        if (jerry_value_is_error(ret_value)) {
            printf("Could not run main script!\n");
            res = 1;
        }

        jerry_release_value(ret_value);
    }
    jerry_release_value(main_app);

    // cleanup engine
    jerry_cleanup();

    return res;
}
